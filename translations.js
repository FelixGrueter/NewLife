function setLanguage(lang) {
    if (!languageData[lang]) return;

    document.documentElement.lang = lang;

    // Translation for standard text
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (languageData[lang][key]) {
            element.innerHTML = languageData[lang][key];
        }
    });

    // Translation for placeholders
    document.querySelectorAll('[data-i18n-placeholder]').forEach(element => {
        const key = element.getAttribute('data-i18n-placeholder');
        if (placeholders[lang] && placeholders[lang][key]) {
            element.setAttribute('placeholder', placeholders[lang][key]);
        }
    });

    // Update active button classes in switcher
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.remove('active');
        if (btn.getAttribute('data-lang') === lang) {
            btn.classList.add('active');
        }
    });

    // Update <select> options if they have data-i18n-select
    document.querySelectorAll('[data-i18n-select]').forEach(selectEl => {
        Array.from(selectEl.options).forEach(option => {
            const key = `mod_option_${option.value}`;
            if (languageData[lang][key]) {
                option.text = languageData[lang][key];
            }
        });
    });

    localStorage.setItem('userLang', lang);
}

document.addEventListener('DOMContentLoaded', () => {
    let savedLang = localStorage.getItem('userLang');
    if (!savedLang) {
        const browserLang = navigator.language || navigator.userLanguage;
        savedLang = browserLang.startsWith('de') ? 'de' : browserLang.startsWith('it') ? 'it' : 'en';
    }
    setLanguage(savedLang);
});
